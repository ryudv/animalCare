package cat;

public class Cat {
	private String catName;
	private String catGender;
	private String catAge;
	private String catType;
	
	private String hospitalName;
	private String visitDate;
	private String symptom;
	private String treatment;
	
	private String recordDate;
	private String weight;
	private String disease;
	private String otherThings;
	
	public String getRecordDate() {
		return recordDate;
	}
	public void setRecordDate(String recordDate) {
		this.recordDate = recordDate;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getDisease() {
		return disease;
	}
	public void setDisease(String disease) {
		this.disease = disease;
	}
	public String getOtherThings() {
		return otherThings;
	}
	public void setOtherThings(String otherThings) {
		this.otherThings = otherThings;
	}
	
	public String getHospitalName() {
		return hospitalName;
	}
	public void setHospitalName(String hospitalName) {
		this.hospitalName = hospitalName;
	}
	public String getVisitDate() {
		return visitDate;
	}
	public void setVisitDate(String visitDate) {
		this.visitDate = visitDate;
	}
	public String getSymptom() {
		return symptom;
	}
	public void setSymptom(String symptom) {
		this.symptom = symptom;
	}
	public String getTreatment() {
		return treatment;
	}
	public void setTreatment(String treatment) {
		this.treatment = treatment;
	}
	
	public String getCatName() {
		return catName;
	}
	public void setCatName(String catName) {
		this.catName = catName;
	}
	public String getCatGender() {
		return catGender;
	}
	public void setCatGender(String catGender) {
		this.catGender = catGender;
	}
	public String getCatAge() {
		return catAge;
	}
	public void setCatAge(String catAge) {
		this.catAge = catAge;
	}
	public String getCatType() {
		return catType;
	}
	public void setCatType(String catType) {
		this.catType = catType;
	}
}
